//Arithmetic Operators

//1. Addition +
//2. Subtract -
//3. Multiplication *
//4. Division /
//5. Modulus  %
//6. Increment ++
//7. Decrement --

var addition = 10 + 5;
console.log('Addition',addition);

var subtraction = 20 - 5;
var subs = 5 - 10;
console.log(subtraction);
console.log(subs);

var multiplication = 11*11;
console.log(multiplication);
var division = 136/5;
console.log(division);
var modulus = 136%5;
console.log(modulus);

//Increment
var x = 10;
x++;  // Post Increment
console.log(x);
var y = 10;
++y; // Pre Increment
console.log(y);

//Decremet

var a = 10;
a--;   // Post Increment
console.log(a);
var b = 10;
--b;
console.log(b);
